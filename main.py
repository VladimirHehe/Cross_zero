def game_board():
    Array_Matrix = [["-" for o in range(4)] for o in range(4)]

    for num in range(1, 4):
        Array_Matrix[num][0] = str(num)

    for num_1 in range(4):
        if num_1 == 0:
            Array_Matrix[0][0] = str(" ")
        else:
            Array_Matrix[0][num_1] = str(num_1)

    for row in range(4):
        print(Array_Matrix[row])


game_board()
