import time
import sys

act_Q = input("Добро пожаловать в игру «Крестики-Нолики»!\n"
              "Чтобы начачть игру нажмите «Q», иначе будет Выход\n"
              " Ввод ----------------->")

active = act_Q == 'Q'


def Begin_Game(func):
    def wrapper():
        if active:
            print("Игра начинается!!!")
            time.sleep(1)
            func()
        else:
            print("Приходите поиграть ещё!\n"
                  "      (*￣▽￣*)ブ")
            sys.exit()

    return wrapper


